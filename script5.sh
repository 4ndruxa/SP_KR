#!/bin/bash

# варіант: 2303 % 6 = 5
# 5) Перенаправити результат виконання утиліти “top” в файл.

# Перевірка наявності параметра n, за замовчуванням n = 5
n=${1:-5}

# Виконання команди "top" та перенаправлення результату у файл top_output.txt
top -b -n 1 > top_output.txt

# Обрізання файлу, залишивши тільки перші n рядків
head -n "$n" top_output.txt
